package com.zhskg.bag.data;

public enum FileStorageEnum {
    TO_FAST_DFS("fastDFS", 0),
    TO_DISK("磁盘", 1);

    private int value;
    private String name;

    FileStorageEnum(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }

}
