package com.zhskg.bag.model;

import java.io.Serializable;

/**
 * Created by luochaojun on 2017/11/4.
 */
public class FileTempEntry implements Serializable {
    private Long fileId;
    private String filePath;
    private String fileType;
    private Integer overdueFlag;
    private Long termValidity;
    private Long createTime;
    private Long createOn;

    public Long getFileId() {
        return fileId;
    }

    public void setFileId(Long fileId) {
        this.fileId = fileId;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public Integer getOverdueFlag() {
        return overdueFlag;
    }

    public void setOverdueFlag(Integer overdueFlag) {
        this.overdueFlag = overdueFlag;
    }

    public Long getTermValidity() {
        return termValidity;
    }

    public void setTermValidity(Long termValidity) {
        this.termValidity = termValidity;
    }

    public Long getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Long createTime) {
        this.createTime = createTime;
    }

    public Long getCreateOn() {
        return createOn;
    }

    public void setCreateOn(Long createOn) {
        this.createOn = createOn;
    }

}
