package com.zhskg.bag.param;

import java.io.Serializable;
import java.util.List;

public class StockParam implements Serializable
{
	private static final long serialVersionUID = 1L;
	
	private List<Integer> productId;
	private String clientId;
	private String stockState;
	private int amount;
	private String orderCode;
	private String qualityState;
	private String productCategory;
	
	private boolean isPage;
	private long startTime;
	private long endTime;
	private int index;
	private int pageSize;
	private String productPro;
	
	public boolean isPage() {
		return isPage;
	}
	public void setPage(boolean isPage) {
		this.isPage = isPage;
	}
	public long getStartTime() {
		return startTime;
	}
	public void setStartTime(long startTime) {
		this.startTime = startTime;
	}
	public long getEndTime() {
		return endTime;
	}
	public void setEndTime(long endTime) {
		this.endTime = endTime;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public int getPageSize() {
		return pageSize;
	}
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}
	public List<Integer> getProductId() {
		return productId;
	}
	public void setProductId(List<Integer> productId) {
		this.productId = productId;
	}
	public String getClientId() {
		return clientId;
	}
	public void setClientId(String clientId) {
		this.clientId = clientId;
	}

	public String getStockState() {
		return stockState;
	}
	public void setStockState(String stockState) {
		this.stockState = stockState;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public String getOrderCode() {
		return orderCode;
	}
	public void setOrderCode(String orderCode) {
		this.orderCode = orderCode;
	}
	public String getQualityState() {
		return qualityState;
	}
	public void setQualityState(String qualityState) {
		this.qualityState = qualityState;
	}
	public String getProductCategory() {
		return productCategory;
	}
	public void setProductCategory(String productCategory) {
		this.productCategory = productCategory;
	}
	public String getProductPro() {
		return productPro;
	}
	public void setProductPro(String productPro) {
		this.productPro = productPro;
	}
}
